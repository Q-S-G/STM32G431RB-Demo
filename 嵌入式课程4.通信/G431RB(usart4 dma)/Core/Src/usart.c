void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
    if (huart == &huart4)                                                                    // 判断串口
    {
        __HAL_UNLOCK(huart);                                                                 // 解锁串口状态
 
        USART4.ReceiveNum  = Size;                                                          // 把接收字节数，存入结构体USART4.ReceiveNum，以备使用
        memset(USART4.ReceiveData, 0, sizeof(USART4.ReceiveData));                         // 清0前一帧的接收数据
        memcpy(USART4.ReceiveData, USART4.BuffTemp, Size);                                 // 把新数据，从临时缓存中，复制到USART4.ReceiveData[], 以备使用
        HAL_UARTEx_ReceiveToIdle_DMA(&huart4, USART4.BuffTemp, sizeof(USART4.BuffTemp));   // 再次开启DMA空闲中断; 每当接收完指定长度，或者产生空闲中断时，就会来到这个
    }
}
